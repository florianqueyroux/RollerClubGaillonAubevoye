        <h2>Histoire du roller hockey</h2>
        <p>Roller Hockey, Roller In-Line, Street Hockey, RILH, Sous ses appellations se cache un sport que vous allez
            découvrir plus en détails ici</p>
        <p>Tout d'abord rappelons le, le Roller Hockey est une discipline qui découle du hockey sur glace, la plus
            grande différence entre ses deux sports est le fait que le roller hockey se pratique sur des rollers
            (patins aux roues alignées).</p>
        <p>Reconnu discipline de Haut Niveau par le Ministère des Sports, le Roller In Line Hockey est un sport
            collectif en plein essor offrant un jeu spectaculaire. Agiles sur leurs rollers, habiles avec leur crosse,
            les hockeyeurs manient le palet et s'affrontent dans des rencontres empreintes de rapidité et de tactique,
            privilégiant la finesse à la dureté physique.</p>
        <p>Le Roller-Hockey est un sport nouveau, dérivé du hockey sur glace, dont il reprend la quasi-totalité de
            l’équipement (crosse, palet et protections).</p>
        <p>Les équipes sont composées de 4 joueurs de champ plus un gardien. Moins rude que le hockey sur glace
            (les mises en échec et les contacts violents sont sanctionnés), il est aussi plus simple dans ses règles
            (pas de hors-jeu).</p>
        <p>Le Roller-Hockey est un sport de technique, de tactique et de rapidité. Les dimensions réduites du terrain
            fermé, rendent toutes les actions et tous les tirs décisifs.</p>

        <p>L’action est permanente : d’un but à l’autre, il se passe toujours quelque chose, ce qui en fait un sport
            spectaculaire.</p>
        <p>Sans surprise, il s'agit de marquer des buts en envoyant le palet (le "puck") dans les buts adverses.
            S'il est autorisé de jouer le palet avec le roller, un but marqué de cette façon n'est pas valable.
            Idem pour un but marqué par un joueur situé dans le demi-cercle placé devant le but, zone nommé
            "zone du gardien", c'est déjà pas mal compliqué hein ?</p>
        <p>L'invention du roller en ligne ainsi que la vague du roller des années 1990 a sans doute donné l'idée aux
            passionnés de hockey de créer un sport permettant de jouer et de s'entrainer sans un nécessaire accès à
            une patinoire.</p>
        <p>Ce jeu déjà bien connu des hockeyeurs qui le pratiquent spontanément dans la rue, arrive pour la première
            fois en France sous forme de compétition structurée lors du premier open de France de Street-hockey,
            organisé par la Fédération en 1993.</p>
        <p>50 clubs sont créés et affiliés à la Fédération qui décide donc de créer, en 1995, un championnat de France
            qui par la suite se déclinera sous forme élite, national 1, 2, 3 et régional.</p>
        <p>Pour pouvoir profiter pleinement de ce sport, il faut respecter un équipement minimum afin de se protéger
            des palets et des crosses adverses qui viendraient malencontreusement se heurter à vous.</p>
        <p>Donc il vous faut :</p>
        <p>Une crosse (soit de bois, soit de carbone, soit d'aluminium, soit de plastique...)</p>
        <p>Une paire de rollers (peut importe leur qualité tant que ça roule c'est OK,)</p>
        <p>Une paire de jambières (qui protègent du haut du pied jusqu'au genou inclus, elles protègent des coups de
            crosse, des jets de palets et des chutes.)</p>
        <p>Une paire de Gants (qui possèdent une large ouverture pour permettre de plier le poignet. Le pouce doit
            contenir une tige rigide pour protéger des chutes)</p>
        <p>Un casque (avec une grille ou une visière intégrale. Seuls les majeurs sont autorisés à ne pas porter de
            protection faciale intégrale)</p>
        <p>Il existe de nombreux autres équipements comme les coudières qui sont obligatoires , il y a également une
            coquille, une culotte (recommandé), un protège dent (selon l'age), une protection thoracique
            (non obligatoire), un pantalon long, un maillot...</p>
        <p>En compétition, tous les équipements doivent être dissimulés sous un pantalon et le maillot excepté pour le
            casque, les gants et les rollers.</p>
        <p>En ce qui concerne le goal l'équipement est bien plus fourni pour pouvoir arreter le palet avec toutes les
            parties du corps sans risqué de se faire mal.</p>
